CREATE TABLE movies (
    Title VARCHAR(255),
    Rating DECIMAL(2, 1),
    Year INT,
    Month VARCHAR(12),
    Certificate VARCHAR(12),
    Runtime VARCHAR(12),
    Directors VARCHAR(255),
    Stars VARCHAR(255),
    Genre VARCHAR(255),
    Filming_locations VARCHAR(255),
    Budget VARCHAR(64),
    Income VARCHAR(64),
    Country_of_origin VARCHAR(255)
);